			{
				const auto iter = mapped_code_consts.find(index);
				if (iter == mapped_code_consts.end())
				{
					ZONETOOL_ERROR("Couldn't map code const %i", index);
				}
			
				return iter->second;
			}

			zonetool::h2::MaterialShaderArgument* convert_shader_arguments(zonetool::h1::MaterialPass* pass, utils::memory::allocator& allocator)
			{
				const auto arg_count = pass->perPrimArgCount + pass->perObjArgCount + pass->stableArgCount;
				const auto converted_args = allocator.allocate_array<zonetool::h2::MaterialShaderArgument>(arg_count);
				std::memcpy(converted_args, pass->args, sizeof(zonetool::h1::MaterialShaderArgument) * arg_count);

				for (auto i = 0; i < arg_count; i++)
				{
					if (converted_args[i].type != MTL_ARG_CODE_CONST)
					{
						continue;
					}

					converted_args[i].u.codeConst.index = convert_code_const(converted_args[i].u.codeConst.index);
				}

				return converted_args;
			}

			template <typename T, typename S>
			T* convert_shader(S* source, utils::memory::allocator& allocator)
			{
				const auto new_shader = allocator.allocate<T>();
				std::memcpy(new_shader, source, sizeof(S));
				new_shader->name = allocator.duplicate_string(game::add_source_postfix(new_shader->name, game::h1));
				return new_shader;
			}

			template <typename T, typename S>
			void convert_shader(T** target, S* source, utils::memory::allocator& allocator)
			{
				if (source == nullptr)
				{
					return;
				}

				*target = convert_shader<T>(source, allocator);
			}

			zonetool::h2::MaterialTechnique* convert(zonetool::h1::MaterialTechnique* technique, utils::memory::allocator& allocator)
			{
				if (technique == nullptr)
				{
					return nullptr;
				}

				const auto size = sizeof(zonetool::h2::MaterialTechniqueHeader) + 
					sizeof(zonetool::h2::MaterialPass) * technique->hdr.passCount;
				const auto new_technique = reinterpret_cast<zonetool::h2::MaterialTechnique*>(allocator.allocate_array<char>(size));
				std::memcpy(new_technique, technique, size);

				new_technique->hdr.name = allocator.duplicate_string(game::add_source_postfix(technique->hdr.name, game::h1));
				new_technique->hdr.flags = technique->hdr.flags;
				new_technique->hdr.passCount = technique->hdr.passCount;

				for (auto i = 0u; i < technique->hdr.passCount; i++)
				{
					const auto new_pass = &new_technique->passArray[i];
					const auto pass = &technique->passArray[i];

					std::memcpy(new_pass, pass, sizeof(zonetool::h1::MaterialPass));

					new_pass->stableArgSize += 16;

					convert_shader(&new_pass->vertexShader, pass->vertexShader, allocator);
					convert_shader(&new_pass->vertexDecl, pass->vertexDecl, allocator);
					convert_shader(&new_pass->hullShader, pass->hullShader, allocator);
					convert_shader(&new_pass->domainShader, pass->domainShader, allocator);
					convert_shader(&new_pass->pixelShader, pass->pixelShader, allocator);

					new_pass->args = convert_shader_arguments(pass, allocator);
				}

				return new_technique;
			}

			zonetool::h2::MaterialTechniqueSet* convert(zonetool::h1::MaterialTechniqueSet* asset, utils::memory::allocator& allocator)
			{
				const auto new_asset = allocator.allocate<zonetool::h2::MaterialTechniqueSet>();

				new_asset->name = allocator.duplicate_string(asset->name);
				COPY_VALUE(flags);
				COPY_VALUE(worldVertFormat);
				COPY_VALUE(preDisplacementOnlyCount);

				const auto remapped_techniques = remap(asset->techniques, allocator);
				for (auto i = 0; i < zonetool::h2::MaterialTechniqueType::TECHNIQUE_COUNT; i++)
				{
					new_asset->techniques[i] = convert(remapped_techniques[i], allocator);
				}

				return new_asset;
			}

